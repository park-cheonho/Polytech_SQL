DROP TABLE 부서;
CREATE TABLE 부서( 부서ID VARCHAR2(2) CONSTRAINT DEPARTMENT_부서ID_PK PRIMARY KEY, 부서명 VARCHAR2(10) -- 부서명
);

DROP TABLE EMPLOYEE;
CREATE TABLE EMPLOYEE ( 
	EMPID VARCHAR2(8), -- 사원 고유 ID
	부서ID VARCHAR2(2), -- 사원 근무 부서 ID CONSTRAINT EMPLOYEE_부서_부서ID_FK FOREIGN KEY(부서ID)
	REFERENCES 부서(부서ID) -- TABLE LEVEL 제약사항
);
DESC 부서;
DESC EMPLOYEE;